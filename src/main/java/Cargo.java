public class Cargo {

    private  int id;
    private int weight;
    private WeightUnit weightUnit;
    private  int pieces;

    public Cargo(){

    }

    public int getWeight() {
        return weight;
    }

    public int getId() {
        return id;
    }

    public WeightUnit getWeightUnit() {
        return weightUnit;
    }

    public int getPieces() {
        return pieces;
    }


}
